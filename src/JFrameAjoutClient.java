    private void boutonValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boutonValiderActionPerformed
        // TODO add your handling code here:
        boolean actualiser=true;
        try{
            String libelle = JFrameAjoutClient.fieldEntreprise.getText();
            String correspondant = JFrameAjoutClient.fieldCorres.getText();
            String tel = JFrameAjoutClient.fieldTel.getText();
            String fax = JFrameAjoutClient.fieldFax.getText();
            String mail = JFrameAjoutClient.fieldMail.getText();
            String adresse = JFrameAjoutClient.fieldAdresse.getText();

            Client clientAjout = new Client(libelle,correspondant,tel,fax,mail,adresse);
            model = (DefaultTableModel) JFrameClients.getjTable2().getModel();
            model.addRow(new Object[]{clientAjout.getLibelle(),clientAjout.getCorrespondant(),clientAjout.getTel(),clientAjout.getFax(),clientAjout.getMail(),clientAjout.getAdresse()});
            
            String s =  System.getProperty("line.separator");
            String clientAjoutString = s+clientAjout.getLibelle()+";"+clientAjout.getCorrespondant()+";"+clientAjout.getTel()+";"+clientAjout.getFax()+";"+clientAjout.getMail()+";"+clientAjout.getAdresse()+";//";
            String clientsString = BddDAO.importClientsFromText();
            String lesClients = clientsString+clientAjoutString;

            ArrayList clients = new ArrayList();
            clients.add(lesClients);
            
            BddDAO.exportClientToText(clients);

            Client unClient = getClient();
            for(int i=0;i<JFrameDevisGest.getjComboBox5().getItemCount();i++){
                if(JFrameDevisGest.getjComboBox5().getItemAt(i).equals(unClient.getLibelle())){
                    actualiser=false;
                }
            } 
            if(actualiser){
                JFrameDevisGest.getjComboBox5().addItem(unClient.getLibelle());
                JFrameDevisGest.getjComboBox5().setSelectedItem(unClient.getLibelle());
                JFrameDevisGest.getjTextField5().setText(unClient.getCorrespondant());
                JFrameDevisGest.getjTextField1().setText(unClient.getTel());
                JFrameDevisGest.getjTextField2().setText(unClient.getFax());  
                JFrameDevisGest.getjTextField4().setText(unClient.getMail());
                JFrameDevisGest.getjTextField6().setText(unClient.getAdresse());
            }
            
            JOptionPane.showMessageDialog(null, "Le client a Ã©tÃ© ajoutÃ©", "Info", JOptionPane.INFORMATION_MESSAGE);
            
            this.dispose();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Un problÃ¨me est survenu", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_boutonValiderActionPerformed

